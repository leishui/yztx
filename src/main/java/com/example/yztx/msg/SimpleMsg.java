package com.example.yztx.msg;

public class SimpleMsg {
    private int status;
    private String msg;

    public SimpleMsg() {
    }

    public SimpleMsg(int status, String msg) {
        this.status = status;
        this.msg = msg;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
